#!/usr/bin/python3
n = 0
while n < 5:
    print(n)
    n += 1


for i in range(5):
    print(i)

for i in range(2, 6):
    print(i)

for i in range(5, 2, -1):
    print(i)

# with decimal
print(5 / 2)

# no decimal round down
print(5 // 2)

import math

print(math.floor(3 / 2))
print(math.ceil(3/2))
print(math.sqrt(2))
print(math.pow(2, 3))
